    private void txtChercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtChercherActionPerformed
        // TODO add your handling code here:

        String titre = txtTitre.getText().trim();

        if (!titre.isEmpty()) {
            model.setRowCount(0); 

            ProjetRecherche projet = prs.findByTire(titre);

            if (projet != null) {
                model.addRow(new Object[]{projet.getId(), projet.getTitre(), projet.getAxe(), projet.getDateDebut(), projet.getDateFin()});
            } else {
                JOptionPane.showMessageDialog(this, "Aucun projet trouvé avec ce titre !");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez entrer un titre !");
        }

    }//GEN-LAST:event_txtChercherActionPerformed


